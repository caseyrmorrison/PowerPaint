package powerpaint;

import java.awt.Color;
import java.awt.Shape;

/**
 * This is a class that stores the shape, color and thickness of the shape 
 * that was drawn on the panel.
 *
 * @author Casey Morrison
 * @version May 21, 2013
 */
public class ToolStorage {
  
  /** The shape that was drawn on the panel. */
  private final Shape my_shape;
  
  /** The thickness of the shape. */
  private final int my_width;
  
  /** The color of the shape. */
  private final Color my_color;

  /**
   * The constructor takes a shape object, a color object 
   * and an integer representing thickness.
   * 
   * @param the_shape - The shape that was drawn on the panel.
   * @param the_color - The color of the shape.
   * @param the_width - The thickness chosen while drawing the shape.
   */
  public ToolStorage(final Shape the_shape, final Color the_color, final int the_width) {
    my_shape = the_shape;
    my_color = the_color;
    my_width = the_width;
  }
  
  /**
   * Returns the shape that was stored in this object.
   * @return my_shape - the shape to be drawn.
   */
  public Shape getShape() {
    return my_shape;
  }

  /**
   * Returns the width of the thickness of the lines of the shape.
   * @return my_width - the thickness of the shape.
   */
  public int getWidth() {
    return my_width;
  }

  /**
   * Returns the color of the object to be drawn.
   * @return my_color - the color the shape was when it was drawn.
   */
  public Color getColor() {
    return my_color;
  } 
}
